package com.android.launcher3.pageindicators.rd.animation;

import android.support.annotation.NonNull;
import com.android.launcher3.pageindicators.rd.animation.controller.AnimationController;
import com.android.launcher3.pageindicators.rd.animation.controller.ValueController;
import com.android.launcher3.pageindicators.rd.draw.data.Indicator;

public class AnimationManager {

    private AnimationController animationController;

    public AnimationManager(@NonNull Indicator indicator, @NonNull ValueController.UpdateListener listener) {
        this.animationController = new AnimationController(indicator, listener);
    }

    public void basic() {
        if (animationController != null) {
            animationController.end();
            animationController.basic();
        }
    }

    public void interactive(float progress) {
        if (animationController != null) {
            animationController.interactive(progress);
        }
    }

    public void end() {
        if (animationController != null) {
            animationController.end();
        }
    }
}
